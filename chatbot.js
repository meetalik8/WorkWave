$(document).ready(function () {

    var user1ans2 = document.querySelector(".user1ans2");
    user1ans2.addEventListener("click", function () {
        setTimeout(function () {
            $("#bot3b").hide();
            $("#bot3b2").hide();
            $("#user3b").hide();
            $("#bot2c").hide();
            $("#botmusic").hide();
            $("#user2b").hide();
            $("#user2c").hide();
            $("#bot4a").hide();
            $("#botaboutus").hide();
            $("#botcommunity").hide();
            $("#bottest").hide();
            $("#botappointment").hide();
            $("#bot2b").show();
            $("#user2b").show();
        }, 500);
    })

    var helpline = document.querySelector(".helpline");
    helpline.addEventListener("click", function () {
        setTimeout(function () {
            $("#bot3b2").hide();
            $("#user3b").hide();

            $("#bot2c").hide();
            $("#botmusic").hide();

            $("#user2c").hide();
            $("#bot4a").hide();
            $("#botaboutus").hide();
            $("#botcommunity").hide();
            $("#bottest").hide();
            $("#botappointment").hide();
            $("#bot3b").show();
        }, 500);

    })
    var suggestions = document.querySelector(".suggestions");
    suggestions.addEventListener("click", function () {
        setTimeout(function () {

            $("#user3b").hide();

            $("#bot2c").hide();
            $("#botmusic").hide();

            $("#user2c").hide();
            $("#bot4a").hide();
            $("#botaboutus").hide();
            $("#botcommunity").hide();
            $("#bottest").hide();
            $("#botappointment").hide();
            $("#bot3b2").show();
        }, 500);

    })

})

